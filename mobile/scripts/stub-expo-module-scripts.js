/**
 * Creates a stub `node_modules/expo-module-scripts` package so the editor stops
 * flagging vendored Expo tsconfigs.
 *
 * Why this exists: several `expo-*` packages (e.g. `expo-secure-store`) ship their
 * own build config — `tsconfig.json` with `"extends": "expo-module-scripts/tsconfig.base"`.
 * `expo-module-scripts` is the *library author's* build tooling; it is NOT a runtime
 * dependency and is intentionally absent from a consumer's install. As a result the
 * `extends` can't resolve and VS Code's TS server reports
 *   "File 'expo-module-scripts/tsconfig.base' not found"
 * on each such vendored tsconfig.
 *
 * This is cosmetic — those tsconfigs are never part of the app's compilation
 * (the app extends `expo/tsconfig.base`), so `tsc --noEmit` is unaffected. The stub
 * simply gives the `extends` a real file to point at, clearing the Problems panel.
 *
 * Generated on `postinstall` (node_modules only changes on install), and skipped if
 * a real `expo-module-scripts` is ever installed, so it never shadows the genuine one.
 */
const fs = require('fs');
const path = require('path');

const projectRoot = __dirname.endsWith('scripts') ? path.resolve(__dirname, '..') : __dirname;
const pkgDir = path.join(projectRoot, 'node_modules', 'expo-module-scripts');
const markerPath = path.join(pkgDir, '.collably-stub');

/** Minimal but valid base config for the vendored tsconfigs to extend. */
const tsconfigBase = {
  compilerOptions: {
    target: 'esnext',
    module: 'esnext',
    moduleResolution: 'bundler',
    strict: true,
    skipLibCheck: true,
    esModuleInterop: true,
  },
};

const stubPackageJson = {
  name: 'expo-module-scripts',
  version: '0.0.0-stub',
  private: true,
  description: 'Local stub generated by scripts/stub-expo-module-scripts.js — not the real package.',
};

function main() {
  if (fs.existsSync(pkgDir) && !fs.existsSync(markerPath)) {
    // A real expo-module-scripts is installed — leave it alone.
    return;
  }
  fs.mkdirSync(pkgDir, { recursive: true });
  fs.writeFileSync(markerPath, 'generated stub — safe to delete\n');
  fs.writeFileSync(path.join(pkgDir, 'package.json'), JSON.stringify(stubPackageJson, null, 2) + '\n');
  fs.writeFileSync(path.join(pkgDir, 'tsconfig.base.json'), JSON.stringify(tsconfigBase, null, 2) + '\n');
  console.log('[stub-expo-module-scripts] wrote stub expo-module-scripts/tsconfig.base.json');
}

main();
